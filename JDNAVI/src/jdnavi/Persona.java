
package jdnavi;

public class Persona {
    String nombre;                  //nombre de la persona
    boolean genero;                 //genero de la persona; True -> Hombre; False -> Mujer
    int edad;                       //edad de la persona

    public Persona(String nombre, boolean genero, int edad) {
        this.nombre = nombre;
        this.genero = genero;
        this.edad = edad;
    }
    
    
}
